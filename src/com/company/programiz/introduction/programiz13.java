package com.company.programiz.introduction;

public class programiz13 {

    //Find Factorial of a number using for loop

    public static void main(String[] args) {
        int num = 10;
        long factoriyel = 1;
        for (int i = 1; i < num; i++) {
            factoriyel *= i;


        }

        System.out.println(factoriyel);
    }
}
